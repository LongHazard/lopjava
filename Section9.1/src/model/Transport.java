package model;

public class Transport {
	private float giaVe;
	private int soGhe;
	
	public Transport(float giaVe, int soGhe) {
		this.giaVe = giaVe;
		this.soGhe = soGhe;
	}
	public float getGiaVe() {
		return giaVe;
	}
	public void setGiaVe(float giaVe) {
		this.giaVe = giaVe;
	}
	public int getSoGhe() {
		return soGhe;
	}
	public void setSoGhe(int soGhe) {
		this.soGhe = soGhe;
	}
	
	

}
